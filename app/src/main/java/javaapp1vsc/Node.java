package javaapp1vsc;

public class Node {
    private int id; 
    private Object value; 
    private Node next;

    public int getId() {
        return id;
    }
    public Object getValue() {
        return value;
    }
    public Node getNext() {
        return next;
    }
    public void setId(int id) {
        this.id = id;
    }
    public void setValue(Object value) {
        this.value = value;
    }
    public void setNext(Node next) {
        this.next = next;
    } 


    

    
    
    
}
